"""Catalog of all 31 agents with their *context of usage*.

This is the knowledge base the MetaAgent uses to decide which agent fits a given
task. Each ``AgentSpec`` records:

* file / class      - how to load and instantiate the agent
* capability        - one line on what it does
* use_when          - the **context of usage**: signals that this agent fits
* inputs            - what you must provide to run it
* not_for           - anti-signals (when NOT to pick it)
* keywords          - extra retrieval terms
* example_task      - a representative request
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import List


@dataclass(frozen=True)
class AgentSpec:
    num: int
    chapter: int
    file: str
    cls: str
    name: str
    capability: str
    use_when: str
    inputs: str
    not_for: str
    keywords: List[str] = field(default_factory=list)
    example_task: str = ""

    def search_text(self) -> str:
        return " ".join([
            self.name, self.capability, self.use_when,
            self.example_task, " ".join(self.keywords),
        ])


